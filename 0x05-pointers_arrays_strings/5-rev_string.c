/**
 * rev_string - prints out the reverse of the string
 * @s: the string to be reversed
 */

void rev_string(char *s)
{
	int i;

	for (i = 0; s[i] != '\0'; i++)
	{
		i = i;
	}
	for (i = i - 1; i >= 0; i--)
	{
	/*	putchar(s[i]); */
		i = i;
	}
	putchar('\n');
}
